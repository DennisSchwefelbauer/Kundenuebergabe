package UebergabeKunde.Constructor;

public class Recontact {
    private String name;
    private String vehicle;

    public Recontact(String name, String vehicle) {
        this.name = name;
        this.vehicle = vehicle;
    }

    public String getName() {
        return name;
    }

    public String getVehicle() {
        return vehicle;
    }
}
